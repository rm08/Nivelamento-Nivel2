package aula;

public class Aluno {
	public String nome;
	public int codigo;
	public Double nota1;
	public Double nota2;
	
	public Aluno() {
	}

	public Aluno(String nome, int codigo, Double nota1, Double nota2) {
		super();
		this.nome = nome;
		this.codigo = codigo;
		this.nota1 = nota1;
		this.nota2 = nota2;
	}
	
}
